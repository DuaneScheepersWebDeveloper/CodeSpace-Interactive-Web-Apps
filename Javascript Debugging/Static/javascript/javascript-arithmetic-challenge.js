// Constant Variables:
const pi = 3.1419;
const half = 0.5;

// Variables for measurements

const rectangleLength = 10;
const rectangleBreadth = 17;
const circleRadius = 25;
const triangleBase = 20;
const triangleHeight = 9;

// Rectangle area calculation
let areaOfRectangle = rectangleLength * rectangleBreadth;

// Append your rectangle answer:
console.log('The area of the given rectangle is : ' + areaOfRectangle);

console.log('\n');

// circle area calculation
let areaOfCircle = pi * circleRadius * circleRadius;

// Append your circle answer:
console.log('The area of the given circle is : ' + areaOfCircle);

console.log('\n');

// triangle area calculation
let areaOfTriangle = half * triangleBase * triangleHeight;

// Append your triangle answer:
console.log('The area of the given triangle is : ' + areaOfTriangle);
